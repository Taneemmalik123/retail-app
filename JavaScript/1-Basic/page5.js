const a =100;
console.log(a);

a = 500;
console.log(a);

/*
Different between var, let, const
=>value change
    ->the var will allow to change the value
    ->the let will allow to change the value
    ->const will not allow to change the value

=>Re-declaration
 ->the var will allow to re-declare the variable with same name
 ->the let will not allow to re-declare the variable with same name
 ->the const will not allow to re-declare the variable with same name


 =>scope of variable
   ->the var will make a globle scope variable
   ->the const and let will make block level scope variable
   */